	const VehicleItem* VehicleItem::fromHash(const hash_t hash) noexcept
	{
		for (const VehicleItem& item : vehicle_items)
		{
			if (rage::atStringHash(item.hash_name) == hash)
			{
				return &item;
			}
		}
		return nullptr;
	}

