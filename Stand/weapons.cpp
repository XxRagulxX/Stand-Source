		all.emplace_back(ATSTRINGHASH("WEAPON_METALDETECTOR"));
		all.emplace_back(ATSTRINGHASH("WEAPON_STINGER"));
		all.emplace_back(ATSTRINGHASH("WEAPON_TRANQUILIZER"));
