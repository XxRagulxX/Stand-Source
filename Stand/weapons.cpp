	std::string Weapon::getDisplayName() const
	{
		return Util::GET_LABEL_TEXT(label_hash_name, false);
	}

	std::string Weapon::getName() const
	{
		auto name = getDisplayName();
		if (disambiguator != 0)
		{
			name.append(" (");
			name.append(Lang::get(disambiguator));
			name.push_back(')');
		}
		return name;
	}

	std::string Weapon::getCategoryName() const
	{
		return getCategoryName(category);
	}

	const Weapon* Weapon::find(const hash_t hash)
	{
		for (const auto& weapon : weapons)
		{
			if (weapon.hash == hash)
			{
				return &weapon;
			}
		}
		return nullptr;
	}

	std::vector<hash_t> Weapon::getAllHashes()
	{
		std::vector<hash_t> all{};
		all.reserve(Weapon::weapons.size());
		for (const auto& weapon : Weapon::weapons)
		{
			all.emplace_back(weapon.hash);
		}
		return all;
	}

