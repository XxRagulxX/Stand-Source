	const char* Names::randFirstName(uint8_t gender)
	{
		if (gender == NAME_MALE)
		{
			return Names::male[soup::rand(0, NAMES_MALE_SIZE)];
		}
		return Names::female[soup::rand(0, NAMES_FEMALE_SIZE)];
	}

	const char* Names::randLastName()
	{
		return Names::last[soup::rand(0, NAMES_LAST_SIZE)];
	}
